import java.util.*;
class PosNegZero
{
	public static void main(String args[])
	{
	int n;
	Scanner s=new Scanner(System.in);
	n=s.nextInt();
	{
		if(n>0){
		System.out.println("no is positive ");
		}
		else if(n<0){
		System.out.println("no is negative ");
		}
		else
		System.out.println("no is zero ");
		}
	}
}